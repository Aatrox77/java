package data_and_operation;

public class Casting {

	public static void main(String[] args) {

		double a= 1.1;
		double b=1;
		System.out.println(b);
		
		double c=1.1;
		int d = (int) 1.1; 
		System.out.println(d);
		// 1 to String
		String e = Integer.toString(1);
		System.out.println(e.getClass());
	}

}
